public class Ravenclaw {


    private String name;
    private int mind;
    private int wisdom;
    private int wit;
    private int creativity;
    private int magic;
    private int transgression;

    public Ravenclaw(String name, int mind, int wisdom, int wit, int creativity, int magic, int transgression) {
        this.name = name;
        this.mind = mind;
        this.wisdom = wisdom;
        this.wit = wit;
        this.creativity = creativity;
        this.magic = magic;
        this. transgression = transgression;
    }

    public String getName() {
        return name;
    }

    public int getMind() {
        return mind;
    }

    public void setMind(int mind) {
        this.mind = mind;
    }

    public int getWisdom() {
        return wisdom;
    }

    public void setWisdom(int wisdom) {
        this.wisdom = wisdom;
    }

    public int getWit() {
        return wit;
    }

    public void setWit(int wit) {
        this.wit = wit;
    }

    public int getCreativity() {
        return creativity;
    }

    public void setCreativity(int creativity) {
        this.creativity = creativity;
    }

    public int getMagic() {
        return magic;
    }

    public void setMagic(int magic) {
        this.magic = magic;
    }

    public int getTransgression() {
        return transgression;
    }

    public void setTransgression(int transgression) {
        this.transgression = transgression;
    }//
}
